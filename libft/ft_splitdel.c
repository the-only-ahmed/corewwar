#include "libft.h"

void	ft_splitdel(char ***split)
{
	int		i;

	if (*split)
	{
		i = 0;
		while ((*split)[i])
		{
			ft_strdel(&((*split)[i]));
			i++;
		}
		*split = NULL;
	}
}
