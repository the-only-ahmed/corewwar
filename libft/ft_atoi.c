#include "libft.h"

int		ft_atoi(const char *s)
{
	int		nb;
	int		sign;
	int		i;

	i = 0;
	sign = 1;
	nb = 0;
	while (s[i] && (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13)))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign = -1;
		i++;
	}
	while (s[i] && ft_isdigit(s[i]) != 0)
	{
		nb = (nb * 10) + (s[i] - 48);
		i++;
	}
	return (nb * sign);
}
