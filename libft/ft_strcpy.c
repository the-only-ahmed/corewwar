#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	int		i;

	i = 0;
	while (s2[i] != '\0')
	{
		s1[i] = s2[i];
		i++;
	}
	while (s1[i] != '\0')
	{
		s1[i] = '\0';
		i++;
	}
	return (s1);
}
