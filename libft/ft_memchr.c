#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t					i;
	const unsigned char		*puc;

	i = 0;
	puc = (const unsigned char *) s;
	while (i < n)
	{
		if (puc[i] == (unsigned char) c)
			return ((void *) (s + i));
		i++;
	}
	return (NULL);
}
